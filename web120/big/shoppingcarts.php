    <h3><strong>Different ways to monetize a website: </strong></h3>
    <ul>
        <li>Affiliate Marketing
        <ul>
           <li>Start by finding a product you like and would recommend. Then on your website, you endorse the product and promote it to your website visitors and email subscribers. If the product or service resonates with these people, they’ll click on your affiliate link, purchasing the product (while you get a split of the sale price).</li> 
        </ul>
        </li>
        <li>Pay per click advertising
        <ul>
           <li>Once you sign up, Google will place a simple code on your website that will identify the content of your site and start displaying relevant advertisements. For example, if your site is about pets (dogs & cats), Google AdSense will start showing your visitors ads for cat food, dog training and more. You get paid each time someone clicks on the ad.</li> 
        </ul>
        </li>
        <li>Sell Ad Space
        <ul>
           <li>Another is to simply sell your own ad space directly to companies looking to sponsor different blogs. You can come up with a price for each space, for example: “Sidebar banner ads will cost $xxx per month”. You can get paid depending on how many visitors you get.</li> 
        </ul>
        </li>
        <li>Sell your own digital product
        <ul>
           <li>Creating good products that are well made and polished require a ton of time and additional resources (like design, content, etc.). There’s a lot of ‘hidden costs’ in both time spent and the contractors to collaborate with. Selling your own products on your site also brings up problematic issues like payment gateways (how are you going to collect payment?), shipping (how are you going to mail or distribute the products?) and taxes</li> 
        </ul>
        </li>
        <li>Accept Donations from Visitors
        <ul>
           <li>If you don’t have a ton of monthly visits, but you do have a strong, engaged community? Simply ask your readers to donate! Accepting one-off donations isn’t a fast road to wealth, but it can help you cover expenses in the short-term if people like what you have to say and want to support your journey.</li> 
        </ul>
        </li>
    </ul>
