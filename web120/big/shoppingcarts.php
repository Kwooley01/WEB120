 <h2 class="pageID">Shopping Carts</h2>
    <p>Shopping Cart Software are systems developed to serve as order catalogues for order stores. In most cases, shopping carts act as the interface between the store’s front end and back end, making it possible for customers to choose products/services, read the description and reviews of what they’ve chosen, add/edit settings, and make a purchase. Shopping cart systems are either independent or parts of a larger eCommerce suite, but for most of them can easily be connected to third-party apps and systems. </p>
